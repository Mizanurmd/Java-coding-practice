package Exam_deloitte_Company;

public class ASCIIValueOfChareater {
	public static void main(String[] args) {
		System.out.println("======ASCII Value Capital Character========");
		for(char i ='A'; i<='Z'; i++) {
			System.out.println(i+":"+(int)i+ " ");
		}
		System.out.println();
		System.out.println("======ASCII Value small Character========");
		for(char i ='a'; i<='z'; i++) {
			System.out.println(i+":"+(int)i+ " ");
		}
	} 
	

}
