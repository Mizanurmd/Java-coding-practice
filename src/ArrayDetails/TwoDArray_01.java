package ArrayDetails;

public class TwoDArray_01 {
	public static void main(String[] args) {
		
		int[][]marks = {{30,35,80},{50,60,40},{70,90,80},{74,95,80}};
		
		
		for(int i =0; i<3; i++) {
			for(int j =0; j<3; j++) {
				System.out.print(marks[i][j]+",");
			}
			System.out.println();
		}
		
		
		
	}
	

}
